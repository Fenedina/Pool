void	ft_swap(int *a, int *b)
{
    int c;
    
	c = *a;
	*a = *b;
	*b = c;
}

int main(void)
{
	int c;
	int a;
	int b;

	a =10;
	b = 2;
	ft_swap(&a, &b);
	printf("%d\n%d\n", a, b);
	return (0);
}
